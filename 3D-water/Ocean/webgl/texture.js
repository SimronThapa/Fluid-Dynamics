// The easiest way to create a texture. Just provide an image URL, and it will
// upload the image (after it loads) to the GPU.
function createTextureFromURL(url) {
  var texture = gl.createTexture();
  texture.image = new Image();
  texture.image.onload = function () { uploadTexture(texture); }
  texture.image.src = url;
  return texture;
}


// Upload an image (stored in texture.image) to the GPU. The image is flipped
// in the Y axis since GL considers (0, 0) to be the bottom left corner. 
// Rotation and mipmap filtering are enabled.
function uploadTexture(texture) {
  gl.bindTexture(gl.TEXTURE_2D, texture);
  gl.pixelStorei(gl.UNPACK_FLIP_Y_WEBGL, true);
  gl.texImage2D(gl.TEXTURE_2D, 0, 
                gl.RGBA, gl.RGBA, 
                gl.UNSIGNED_BYTE, 
                texture.image);
  texture.image = null;   // allow garbage collection
  gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_MAG_FILTER, gl.LINEAR);
  gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_MIN_FILTER, gl.LINEAR_MIPMAP_LINEAR);
  gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_WRAP_S, gl.REPEAT);
  gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_WRAP_T, gl.REPEAT);
  gl.generateMipmap(gl.TEXTURE_2D);
}

